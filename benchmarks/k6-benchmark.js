import http from "k6/http";
import { check, sleep, group } from "k6";
import { Trend, Rate, Counter } from "k6/metrics";

// ── Configurable via environment variables ──
const BASE_URL = __ENV.BASE_URL || "http://localhost:4000";
const BENCHMARK_TOKEN = __ENV.BENCHMARK_TOKEN || "";
const VUS = parseInt(__ENV.VUS) || 10;
const DURATION = __ENV.DURATION || "30s";

export const options = {
  vus: VUS,
  duration: DURATION,
  thresholds: {
    http_req_duration: ["p(50)<500", "p(95)<2000", "p(99)<5000"],
    http_req_failed: ["rate<0.1"],
  },
  summaryTrendStats: ["min", "avg", "med", "p(50)", "p(90)", "p(95)", "p(99)", "max"],
};

// ── Custom metrics ──
const ttfb = new Trend("ttfb");
const p50Latency = new Trend("p50_latency");
const p95Latency = new Trend("p95_latency");
const p99Latency = new Trend("p99_latency");
const errorRate = new Rate("error_rate");
const rpsMetric = new Counter("requests");

// ── Auth headers ──
function authHeaders() {
  return BENCHMARK_TOKEN
    ? { Authorization: `Bearer ${BENCHMARK_TOKEN}`, "Content-Type": "application/json" }
    : { "Content-Type": "application/json" };
}

// ── Helper: measure an endpoint ──
function measureEndpoint(method, path, body = null, tags = {}) {
  const params = { headers: authHeaders(), tags };
  if (body) params.body = JSON.stringify(body);

  const start = Date.now();
  const res = method === "GET" ? http.get(`${BASE_URL}${path}`, params)
    : method === "POST" ? http.post(`${BASE_URL}${path}`, body ? JSON.stringify(body) : null, params)
    : method === "PUT" ? http.put(`${BASE_URL}${path}`, body ? JSON.stringify(body) : null, params)
    : http.del(`${BASE_URL}${path}`, params);

  const elapsed = Date.now() - start;

  // Record metrics
  ttfb.add(res.timings.waiting);
  p50Latency.add(res.timings.duration);
  p95Latency.add(res.timings.duration);
  p99Latency.add(res.timings.duration);
  errorRate.add(res.status >= 400);
  rpsMetric.add(1);

  check(res, {
    [`${method} ${path} status ok`]: (r) => r.status < 500,
    [`${method} ${path} response time < 5s`]: (r) => r.timings.duration < 5000,
  });

  sleep(0.1);
  return res;
}

