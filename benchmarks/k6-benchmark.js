// ── Summary output ──
export function handleSummary(data) {
  // ── Extract metrics per endpoint ──
  const endpoints = {};
  if (data.metrics.http_req_duration) {
    const dur = data.metrics.http_req_duration;
    endpoints["all"] = {
      p50_ms: dur.values ? dur.values["p(50)"] : null,
      p95_ms: dur.values ? dur.values["p(95)"] : null,
      p99_ms: dur.values ? dur.values["p(99)"] : null,
      avg_ms: dur.values ? dur.values.avg : null,
      min_ms: dur.values ? dur.values.min : null,
      max_ms: dur.values ? dur.values.max : null,
    };
  }

  if (data.metrics.http_req_failed) {
    const failed = data.metrics.http_req_failed;
    endpoints["all"].error_rate_pct = failed.values
      ? (failed.values.rate * 100).toFixed(2)
      : null;
  }

  if (data.metrics.http_reqs) {
    endpoints["all"].total_requests = data.metrics.http_reqs.values?.count || 0;
  }

  const duration_sec = data.state?.testRunDurationMs
    ? (data.state.testRunDurationMs / 1000).toFixed(1)
    : "N/A";

  endpoints["all"].rps = endpoints["all"].total_requests / parseFloat(duration_sec) || 0;

  // ── JSON output ──
  const jsonOutput = {
    timestamp: new Date().toISOString(),
    base_url: BASE_URL,
    vus: VUS,
    duration: DURATION,
    actual_duration_sec: duration_sec,
    endpoints,
    environment: {
      node_version: "k6",
      hostname: __ENV.HOSTNAME || "unknown",
    },
  };

  // ── Markdown summary ──
  const md = `
# API Benchmark Report

**Generated:** ${jsonOutput.timestamp}  
**Target:** ${BASE_URL}  
**Load:** ${VUS} VUs × ${DURATION}

## Overall Metrics

| Metric | Value |
|--------|-------|
| Total Requests | ${endpoints.all.total_requests || "N/A"} |
| Duration | ${duration_sec}s |
| Avg RPS | ${endpoints.all.rps?.toFixed(1) || "N/A"} |
| Error Rate | ${endpoints.all.error_rate_pct || "N/A"}% |

## Latency (All Endpoints)

| Percentile | Latency |
|------------|---------|
| p50 (median) | ${endpoints.all.p50_ms?.toFixed(2) || "N/A"} ms |
| p95 | ${endpoints.all.p95_ms?.toFixed(2) || "N/A"} ms |
| p99 | ${endpoints.all.p99_ms?.toFixed(2) || "N/A"} ms |
| Avg | ${endpoints.all.avg_ms?.toFixed(2) || "N/A"} ms |
| Min | ${endpoints.all.min_ms?.toFixed(2) || "N/A"} ms |
| Max | ${endpoints.all.max_ms?.toFixed(2) || "N/A"} ms |

## Threshold Verification

| Check | Status |
|-------|--------|
| p50 < 500ms | ${(endpoints.all.p50_ms || 9999) < 500 ? "✅ PASS" : "❌ FAIL"} |
| p95 < 2000ms | ${(endpoints.all.p95_ms || 9999) < 2000 ? "✅ PASS" : "❌ FAIL"} |
| p99 < 5000ms | ${(endpoints.all.p99_ms || 9999) < 5000 ? "✅ PASS" : "❌ FAIL"} |
| Error Rate < 10% | ${parseFloat(endpoints.all.error_rate_pct || 100) < 10 ? "✅ PASS" : "❌ FAIL"} |

> Generated by k6 benchmark suite. See \`/benchmarks/results/\` for raw JSON.
`;

  return {
    "benchmarks/results/benchmark-summary.json": JSON.stringify(jsonOutput, null, 2),
    "benchmarks/results/benchmark-summary.md": md,
  };
}
